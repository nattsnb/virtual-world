export function checkKeyPressed(evt) {
    if (evt.keyCode == "87") {
        alert("You pressed W");
    }
    if (evt.keyCode == "65") {
        alert("You pressed A");
    }
    if (evt.keyCode == "83") {
        alert("You pressed S");
    }
    if (evt.keyCode == "68") {
        alert("You pressed D");
    }
}